
class Areal {


    public static void main (String[] args) {

        int bredde = 10;
        int lengde = 10;

        System.out.println("Bredde: " + bredde);
        System.out.println("Lengde: " + lengde);

        int areal = lengde * bredde;
        System.out.println("Areal: " + areal);

    }

}
